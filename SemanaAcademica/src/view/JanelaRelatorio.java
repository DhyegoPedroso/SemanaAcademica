    public void atualizarTabela() {
        try {
            DefaultTableModel modeloAuto = (DefaultTableModel) TabelaRelatorio.getModel();
            modeloAuto.setNumRows(0);
            RelatorioImpl impl = new RelatorioImpl();
            for (Relatorio r : impl.pesquisarAll()) {
                SimpleDateFormat dataHora = new SimpleDateFormat("dd/MM/yyyy     HH:mm:ss");
                modeloAuto.addRow(new Object[]{
                    r.getId(),
                    r.getAluno().getNome(),
                    r.getAluno().getNomeCurso(),
                    r.getAluno().getFaseCurso(),
                    r.getPalestra().getTitulo(),
                    r.getPalestra().getDia(),
                    dataHora.format(r.getData())});
            }
        } catch (SQLException ex) {
            Logger.getLogger(JanelaRelatorio.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void expExcel() {
        Integer rows = TabelaRelatorio.getModel().getRowCount();
        if (rows > 0) {
            JFileChooser seleccionar = new JFileChooser();
            File arquivo;
            if (seleccionar.showDialog(null, "Exportar Excel") == JFileChooser.APPROVE_OPTION) {
                arquivo = seleccionar.getSelectedFile();
                int cantFila = TabelaRelatorio.getRowCount();
                int cantColumna = TabelaRelatorio.getColumnCount();
                Workbook wb;
                wb = new HSSFWorkbook();
                Sheet folha = wb.createSheet("  ");

                try {
                    for (int i = -1; i < cantFila; i++) {
                        Row fila = folha.createRow(i + 1);
                        for (int j = 0; j < cantColumna; j++) {
                            Cell celda = fila.createCell(j);
                            if (i == -1) {
                                celda.setCellValue(String.valueOf(TabelaRelatorio.getColumnName(j)));
                            } else {
                                celda.setCellValue(String.valueOf(TabelaRelatorio.getValueAt(i, j)));
                            }
                            wb.write(new FileOutputStream(arquivo + ".xls"));
                        }
                    }
                    JOptionPane.showMessageDialog(null, "Planilha exportada com sucesso.");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Por favor tente novamente" + e);
                }
            }
        }
    }

