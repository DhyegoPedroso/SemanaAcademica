    public void atualizarTabela() {
        try {
            DefaultTableModel modeloAuto = (DefaultTableModel) TabelaAlunos.getModel();
            modeloAuto.setNumRows(0);
            AlunoImpl impl = new AlunoImpl();
            for (Aluno a : impl.pesquisarAll()) {
                modeloAuto.addRow(new Object[]{
                    a.getId(),
                    a.getNome(),
                    a.getNomeCurso(),
                    a.getFaseCurso()});
            }
        } catch (SQLException ex) {
            Logger.getLogger(JanelaAlunos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void pesquisarNome(String nome) {
        try {
            DefaultTableModel modeloAuto = (DefaultTableModel) TabelaAlunos.getModel();
            modeloAuto.setNumRows(0);
            AlunoImpl impl = new AlunoImpl();
            for (Aluno a : impl.pesquisarPorNome(nome)) {
                modeloAuto.addRow(new Object[]{
                    a.getId(),
                    a.getNome(),
                    a.getNomeCurso(),
                    a.getFaseCurso()});
            }
        } catch (SQLException ex) {
            Logger.getLogger(JanelaAlunos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

