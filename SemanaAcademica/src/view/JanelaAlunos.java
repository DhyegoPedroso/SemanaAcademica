    private void TabelaAlunosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaAlunosMouseClicked
        if (TabelaAlunos.getSelectedRow() != -1) {
            String valor = this.TabelaAlunos.getValueAt(TabelaAlunos.getSelectedRow(), 0).toString();
            int id = Integer.parseInt(valor);
            AlunoImpl alunoImpl = new AlunoImpl();
            try {
                List<Aluno> alunos = (List<Aluno>) alunoImpl.pesquisarPorId(id);
                for (Aluno aluno : alunos) {
                    TxtNomeAluno.setText(aluno.getNome());
                    TxtNomeCurso.setText(aluno.getNomeCurso());
                    TxtFaseCurso.setText(String.valueOf(aluno.getFaseCurso() + "º"));
                    ativarComponentes();
                }
            } catch (SQLException ex) {
                Logger.getLogger(JanelaAlunos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_TabelaAlunosMouseClicked

    private void BtnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnValidarActionPerformed

        if (BoxPalestra.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma palestra!!");
        } else {
            try {
                if (TabelaAlunos.getSelectedRow() != -1) {
                    String valor = this.TabelaAlunos.getValueAt(TabelaAlunos.getSelectedRow(), 0).toString();
                    int id = Integer.parseInt(valor);

                    Aluno aluno = new Aluno();
                    aluno.setId(id);

                    Palestra palestra = (Palestra) BoxPalestra.getSelectedItem();
                    palestra.setId(palestra.getId());

                    Relatorio relatorio = new Relatorio();
                    RelatorioImpl relatorioImpl = new RelatorioImpl();
                    relatorio.setAluno(aluno);
                    relatorio.setPalestra(palestra);
                    relatorioImpl.salvar(relatorio);

                    limparCampos();
                    desativarComponentes();
                }
            } catch (SQLException ex) {
                Logger.getLogger(JanelaAlunos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_BtnValidarActionPerformed

